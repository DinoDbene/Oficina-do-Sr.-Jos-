    private void bSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bSalvarActionPerformed
       
    // CRIAR E INICIALIZAR OBJETOS  
    
    Carro c = new Carro();
        
        c.setPlaca(jTxtPlaca.getText());
        c.setModelo(jTxtModelo.getText());
    
    Cliente cl = new Cliente();
        
        cl.setNome(jTxtNome.getText());
        cl.setSexo(jCbSexo.getSelectedIndex());
        
    
    Telefone te = new Telefone();
        
        te.setNumero(jTxtTelefone.getText());
        
    Cor co = new Cor();        
 
            if (jCheckBoxCadCor.isSelected()) {
                co.setCor(jTfNovaCor.getText());
            } else {
                co.setCor((String)jCbCor.getSelectedItem());
            }    
    // VALIDADAR ENTRADA
        
        if (c.modeloIsReady() && c.placaIsReady(true) && cl.isReady() && te.isReady() && 
           ((!jCheckBoxCadCor.isSelected()) || (jCheckBoxCadCor.isSelected() && co.isReady()))){
    
        // INSERIR NO BANCO
            System.out.println("Inserindo no banco s√≥ de mentirinha...");
            
            c.setIdcarro(CarroDao.create(c));  
                try {
                    cl.setCarro(c);
                } catch (SQLException ex) {
                    Logger.getLogger(Cadastro.class.getName()).log(Level.SEVERE, null, ex);
                }
            cl.setIdcliente(ClienteDao.create(cl));
            te.setIdtelefone(TelefoneDao.create(te));

            if (jCheckBoxCadCor.isSelected()) {
                co.setIdcor(CorDao.create(co));
            } else {
                co.setIdcor(CorDao.createId(co));
            }

            Carro_Cor_Dao.create(c.getIdcarro(), co.getIdcor());
            Cliente_Telefone_Dao.create(cl.getIdcliente(), te.getIdtelefone());                
        
        // LIMPAR CAMPOS
    
        jTxtNome.setText("");
        jTxtModelo.setText("");
        jTxtPlaca.setText("");
        jTxtTelefone.setText("");
        jTfNovaCor.setText("");
        ValidationModelo.setVisible(false);
        ValidationCor.setVisible(false);
        ValidationNome.setVisible(false);
        ValidationPlaca.setVisible(false);
        ValidationTelefone.setVisible(false);

        atualizarComboBox();

        } else {
            System.out.println("Entrada invalida");
            
            if (!c.modeloIsReady()) {
                ValidationModelo.setVisible(true);
            } else {
                ValidationModelo.setVisible(false);
            }
            
            if (!c.placaIsReady(true)) {
                ValidationPlaca.setVisible(true);
            } else {
                ValidationPlaca.setVisible(false);
            }
            
            if (!cl.isReady()) {
                ValidationNome.setVisible(true);
            } else {
                ValidationNome.setVisible(false);
            }
            
            if (!te.isReady()) {
                ValidationTelefone.setVisible(true);
            } else {
                ValidationTelefone.setVisible(false);
            }
            
            if (jCheckBoxCadCor.isSelected() && !co.isReady()) {
                ValidationCor.setVisible(true);
            } else {
                ValidationCor.setVisible(false);
            }   
        }        
    }//GEN-LAST:event_bSalvarActionPerformed

